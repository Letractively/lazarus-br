CREATE TABLE BIOLIFE
(
  SPECIESNO Float NOT NULL,
  CATEGORY Varchar(15),
  COMMON_NAME Varchar(30),
  SPECIESNAME Varchar(40),
  LENGTHCM Float,
  LENGTH_IN Float,
  NOTES Blob sub_type 1,
  GRAPHIC Blob sub_type 0,
  CONSTRAINT BIOLIFE_PRIMARY PRIMARY KEY (SPECIESNO)
);
CREATE TABLE CUSTOMER
(
  CUSTNO Float NOT NULL,
  COMPANY Varchar(30),
  ADDR1 Varchar(30),
  ADDR2 Varchar(30),
  CITY Varchar(15),
  STATE Varchar(20),
  ZIP Varchar(10),
  COUNTRY Varchar(20),
  PHONE Varchar(15),
  FAX Varchar(15),
  TAXRATE Float,
  CONTACT Varchar(20),
  LASTINVOICEDATE Timestamp,
  CONSTRAINT CUSTOMER_PRIMARY PRIMARY KEY (CUSTNO)
);
CREATE TABLE ORDERS
(
  ORDERNO Float NOT NULL,
  CUSTNO Float NOT NULL,
  SALEDATE Timestamp,
  SHIPDATE Timestamp,
  EMPNO Integer NOT NULL,
  SHIPTOCONTACT Varchar(20),
  SHIPTOADDR1 Varchar(30),
  SHIPTOADDR2 Varchar(30),
  SHIPTOCITY Varchar(15),
  SHIPTOSTATE Varchar(20),
  SHIPTOZIP Varchar(10),
  SHIPTOCOUNTRY Varchar(20),
  SHIPTOPHONE Varchar(15),
  SHIPVIA Varchar(7),
  PO Varchar(15),
  TERMS Varchar(6),
  PAYMENTMETHOD Varchar(7),
  ITEMSTOTAL Numeric(15,4),
  TAXRATE Float,
  FREIGHT Numeric(15,4),
  AMOUNTPAID Numeric(15,4),
  CONSTRAINT ORDERS_PRIMARY PRIMARY KEY (ORDERNO)
);

